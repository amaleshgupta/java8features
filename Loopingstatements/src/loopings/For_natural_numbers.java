package loopings;

import java.util.Scanner;

public class For_natural_numbers {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("enter any number.To print the natural numbers");
		int a=sc.nextInt();
		int i;
		for(i=0; i<=a; i++) {
			
			System.out.print(+a);
			System.out.println("");
			
		}
		
	}

}
